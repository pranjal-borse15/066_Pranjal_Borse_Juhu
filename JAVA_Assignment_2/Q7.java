import java.util.*;


public class Mul{
	public static void main(String args[]){
		int ans = 0;
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the number:");
		int a = sc.nextInt();
		
		for(int i=1;i<=10;i++){
			ans = a * i;
			System.out.println(+a+"*"+i+"="+ans);
			}
		}
	}
		
		
