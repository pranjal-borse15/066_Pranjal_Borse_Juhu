class Q23{
	public static void main(String args[])
	{
		for(int i=5;i>=1;i--)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print("*");
			}
		System.out.println();
		}
		for(int l=6;l<=9;l++)
		{
			for(int m=5;m<=l;m++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
	}
}

